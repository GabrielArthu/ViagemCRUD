package modelo;

public class PassagemPromocao {
	private int idPassagem;
	private String ufDestino;
	private String portaoEmbarque;
	private String empresaAeria;
	private String poutrona;
	
	
	public int getIdPassagem() {
		return idPassagem;
	}
	public void setIdPassagem(int idPassagem) {
		this.idPassagem = idPassagem;
	}
	public String getUfDestino() {
		return ufDestino;
	}
	public void setUfDestino(String ufDestino) {
		this.ufDestino = ufDestino;
	}
	public String getPortaoEmbarque() {
		return portaoEmbarque;
	}
	public void setPortaoEmbarque(String portaoEmbarque) {
		this.portaoEmbarque = portaoEmbarque;
	}
	public String getEmpresaAeria() {
		return empresaAeria;
	}
	public void setEmpresaAeria(String empresaAeria) {
		this.empresaAeria = empresaAeria;
	}
	public String getPoutrona() {
		return poutrona;
	}
	public void setPoutrona(String poutrona) {
		this.poutrona = poutrona;
	}
	
	
	
}
